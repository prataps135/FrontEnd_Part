var a = 5;
var b = 2;

// arthimetic operators
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);

// relational operators
console.log(a < b);
console.log(a > b);
console.log(a >= b);
console.log(a <= b);
console.log(a == b);
console.log(a === b);
console.log(a != b);
console.log(a !== b);

// Increment & Decrement
console.log(a++);
console.log(a--);
console.log(++a);
console.log(--a);

// Short hands
a += 10;
a -= 10;
a *= 10;
a /= 10;
a %= 10;

// Logical operators
console.log( (a == 5) && (b == 2) );
console.log( true && true);
console.log( true && false);
console.log( true || false);
console.log( false || false);
console.log( !(true) );
console.log( !(false) );

