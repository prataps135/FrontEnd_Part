var a = 0;
var b = 5;

// If else condition
if (a > b) {
    console.log("A is greater than B");
} else if (a < b) {
    console.log("B is greater than A");
} else {
    console.log("Both are equal");
}

// Switch case condition
// numeric and char/String
switch (a) {
    case 1:
        console.log("case 1");
        break;
    case 2:
        console.log("case 2");
        break;
    case 3:
        console.log("case 3");
        break;
    case 4:
        console.log("case 4");
        break;
    case 5:
        console.log("case 5");
        break;
    default :
        console.log("Default")
}